CREATE TABLE user(
id VARCHAR(50) PRIMARY KEY,
name VARCHAR(50),
password VARCHAR(50),
height INT(10),
weight INT(10),
bloodtypeABO VARCHAR(10),
bloodtypeRh VARCHAR(10),
guardianTel VARCHAR(50)
);

CREATE TABLE disease(
diseaseId INT PRIMARY KEY auto_increment,
userId VARCHAR(50) NOT NULL,
category VARCHAR(50),
value VARCHAR(50),
diseaseDate date,
FOREIGN KEY(userId) REFERENCES user(id)
on delete cascade
);

